% Task 1: эквивалентная ПФ структурной схемы рис.1
% Метод: символьная свёртка + численная проверка через tf/feedback

clc; clear; close all;

s = tf('s');

% Блоки
W1 = 0.7/(1.5*s + 1);            % фильтр на входе
W2 = 3030/(0.01*s);              % интегральный регулятор тока
W3 = 10;                         % усилитель
W4 = 1/(0.1*s);                  % механический интегратор
H  = 1/(0.05*s + 1);             % датчик в обратной связи
K13 = 13;                        % параллельная пропорциональная ветвь

% Шаг 1: параллельное соединение W1 и K13 (на сумматор "++")
W_par = W1 + K13;
disp('W_par = W1 + 13 ='); zpk(W_par)

% Шаг 2: внутренний контур с единичной отрицательной ОС
W23 = W2*W3;
W_inner = feedback(W23, 1);
disp('W_inner ='); zpk(W_inner)

% Шаг 3: последовательно с W4
W_forward = W_par * W_inner * W4;
disp('W_forward (прямая ветвь) ='); zpk(W_forward)

% Шаг 4: внешняя обратная связь через H
W_cl = feedback(W_forward, H);
W_cl = minreal(W_cl);
disp('W_cl (замкнутая ПФ) ='); tf(W_cl)

% Контроль: переходная характеристика
figure('Position',[100 100 800 450]);
step(W_cl, 30);
title('Task 1: переходная характеристика замкнутой системы');
grid on;
saveas(gcf, 'task1_step.png');

% Сохраняем W_cl для использования в других task
save('W_cl.mat', 'W_cl', 'W_forward');
disp('W_cl сохранён в W_cl.mat');
